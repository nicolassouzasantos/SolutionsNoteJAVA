CREATE TABLE IF NOT EXISTS AUTOMOVEL (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PLACA VARCHAR(20) NOT NULL,
    CHASSI VARCHAR(50) NOT NULL,
    TIPO VARCHAR(100) NOT NULL,
    COR VARCHAR(50),
    LOCALIZACAO_NO_PATIO VARCHAR(150),
    COMENTARIOS CLOB,
    PATIO_ID BIGINT,
    CONSTRAINT FK_AUTOMOVEL_PATIO FOREIGN KEY (PATIO_ID) REFERENCES PATIO (ID)
);

CREATE INDEX IF NOT EXISTS IDX_AUTOMOVEL_PLACA ON AUTOMOVEL (PLACA);
CREATE INDEX IF NOT EXISTS IDX_AUTOMOVEL_TIPO ON AUTOMOVEL (TIPO);
